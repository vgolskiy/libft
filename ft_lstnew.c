#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list *res;

	res = (t_list *)malloc(sizeof(t_list));
	if (res == 00)
		return (00);
	res->content = content;
	res->next = 00;
	return (res);
}
