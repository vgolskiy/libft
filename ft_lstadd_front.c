#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	t_list *current;

	current = *lst;
	if ((!current) && (new))
		*lst = new;
	else if ((current) && (new))
	{
		new->next = current;
		*lst = new;
	}
	return ;
}
