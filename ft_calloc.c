#include "libft.h"

void	*ft_calloc(size_t num, size_t size)
{
	void *res;

	if ((!num) || (!size))
	{
		num = 1;
		size = 1;
	}
	res = malloc(num * size);
	if (!res)
		return (00);
	ft_bzero(res, num * size);
	return (res);
}
