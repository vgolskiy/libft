#include "libft.h"

int		ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*list;

	i = 0;
	if (lst != 00)
	{
		list = lst;
		while (list)
		{
			i++;
			list = list->next;
		}
	}
	return (i);
}
