#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list *current;
	t_list *next;

	if (*lst)
	{
		current = *lst;
		if (del)
		{
			while (current)
			{
				next = current->next;
				ft_lstdelone(current, del);
				current = next;
			}
			*lst = 00;
		}
	}
	return ;
}
